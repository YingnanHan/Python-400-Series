# 为了解决浅拷贝不可以完全复制一个对象的情况，引入深拷贝
# 可以将被拷贝的对象的全部内容完全复制一份

import copy

a = [10, 20, 30, 40, [50, 60, 70, 80]]
b = copy.deepcopy(a)  # 使用深拷贝完全创建一个独立的另一份完整的a对象，包含了a下所有的对象
print("a:", a)
print("b:", b)

b.append(30)
b[4].append(90)
print("深拷贝......")
print("a:", a)  # a: [10, 20, 30, 40, [50, 60, 70, 80], 30]
print("b:", b)  # b: [10, 20, 30, 40, [50, 60, 70, 80, 90], 30]
